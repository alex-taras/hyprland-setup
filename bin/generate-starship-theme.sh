format = '[[ \$symbol \$branch ](fg:color_fg0 bg:color_aqua)](\$style)'

[git_status]
style = "bg:color_aqua"
format = '[[(\$all_status\$ahead_behind )](fg:color_fg0 bg:color_aqua)](\$style)'

[nodejs]
symbol = ""
style = "bg:color_blue"
format = '[[ \$symbol( \$version) ](fg:color_fg0 bg:color_blue)](\$style)'

[c]
symbol = " "
style = "bg:color_blue"
format = '[[ \$symbol( \$version) ](fg:color_fg0 bg:color_blue)](\$style)'

[rust]
symbol = ""
style = "bg:color_blue"
format = '[[ \$symbol( \$version) ](fg:color_fg0 bg:color_blue)](\$style)'

[golang]
symbol = ""
style = "bg:color_blue"
format = '[[ \$symbol( \$version) ](fg:color_fg0 bg:color_blue)](\$style)'

