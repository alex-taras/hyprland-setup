#!/bin/bash

# Generate starship theme from theme definition
# Usage: generate-starship-theme.sh <theme-name>

THEME=${1:-gruvbox}
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
THEME_DIR="$(dirname "$SCRIPT_DIR")/themes"
THEME_FILE="$THEME_DIR/$THEME.conf"
STARSHIP_FILE="$HOME/.config/starship.toml"

if [ ! -f "$THEME_FILE" ]; then
    echo "Error: Theme file not found: $THEME_FILE"
    exit 1
fi

# Source the theme file
source "$THEME_FILE"

# Backup existing starship config if not already backed up
if [ -f "$STARSHIP_FILE" ] && [ ! -f "$STARSHIP_FILE.bak" ]; then
    cp "$STARSHIP_FILE" "$STARSHIP_FILE.bak"
fi

# Generate palette section for starship
cat > "$STARSHIP_FILE" << 'STARSHIP_CONFIG'
# Starship prompt configuration
# Generated by theme-switcher

format = """
[](color_orange)\
$os\
$username\
[](bg:color_yellow fg:color_orange)\
$directory\
[](fg:color_yellow bg:color_aqua)\
$git_branch\
$git_status\
[](fg:color_aqua bg:color_blue)\
$c\
$rust\
$golang\
$nodejs\
$php\
$java\
$kotlin\
$haskell\
$python\
[](fg:color_blue bg:color_bg3)\
$docker_context\
$conda\
[](fg:color_bg3 bg:color_bg1)\
$time\
[ ](fg:color_bg1)\
$line_break$character"""

STARSHIP_CONFIG

# Append palette
cat >> "$STARSHIP_FILE" << EOF

[palettes.theme_colors]
color_fg0 = '$fg0'
color_bg1 = '$bg1'
color_bg3 = '$bg3'
color_blue = '$info'
color_aqua = '$aqua'
color_green = '$success'
color_orange = '$accent'
color_purple = '$purple'
color_red = '$critical'
color_yellow = '$warning'

palette = 'theme_colors'

[os]
